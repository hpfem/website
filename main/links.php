<h1>FEM codes and resources</h1>

<h3>hp-FEM codes</h3>
<p>
Besides Hermes, there are very few hp-FEM codes we are aware of:
<ul>
    <li><a href="http://math.nist.gov/phaml">Phaml</a> (developed by William Mitchell at NIST)</li>
    <li><a href="http://www.concepts.math.ethz.ch/">Concepts</a> (developed by Ph.D. students at SAM, ETH Zurich)</li>
    <li><a href="http://users.ices.utexas.edu/~leszek/hp-instructions.html">1Dhp90, 2Dhp90, 3Dhp90</a> (developed by L. Demkowicz at UT Austin)</li>
</ul> 

If you know of any other opensource adaptive hp-FEM code that should be here, 
please let us know!<br/><br/>
</p>

<h3>Standard FEM codes</h3> 
<p>
There are numerous (standard low-order) FEM codes available on the web. We only list some of them:
<ul>
    <li><a href="http://sfepy.org/">SfePy</a></li>
    <li><a href="http://libmesh.sf.net/">libMesh</a></li>
    <li><a href="http://dealii.org/">deal.II</a></li>
    <li><a href="http://fenics.org/">FEniCS Project</a></li>
    <li><a href="http://oofem.org/">OOFEM</a></li>
    <li><a href="http://sourceforge.net/projects/ngsolve/">NGSolve</a></li>
    <li><a href="http://home.gna.org/getfem/">Getfem++</a></li>
    <li><a href="https://launchpad.net/escript-finley/">Escript/Finley</a></li>    
</ul> 

If you know of any other opensource FEM code that should be here, 
please let us know!<br/><br/>
</p>

