<h3>Implement Graphical Calculus Applications in Javascript</h3>

<p>
Tons of them are already available, often as Java applets. 
We want to have all of them in the Online Lab, equally interactive and possibly nicer. 
See for example <a href="http://www.math.psu.edu/dlittle/java/calculus/index.html">this page</a>,
<a href="http://calculusapplets.com/">this page</a>, or
<a href="http://www.slu.edu/classes/maymk/MathApplets-SLU.html">this page</a>.
</p>

<h3>Make Free Logical/Puzzle Games Part of the Online Lab</h3>

<p>
We are interested in these games to make the Online Lab more 
