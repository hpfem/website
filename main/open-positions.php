<h1>Jobs</h1>

<h3>WebGL 3D Geometry Modeling</h3>

<p>
WebGL is an essential component in the Online Lab but we still define 
3D surfaces via elementary surface triangles. We need to be able to 
define 3D geometries easier, similarly to commercial softwares such as
Inventor or Solid Edge.
</p>

<h3>Create Admin Interface to the Online Lab</h3>

<p>
We need a Django-based admin interface that will allow us to monitor
how many people are logged in to the Online Lab, how much CPU they are 
using, how much memory, how much hard disk space they are using, etc.
We also need to access statistics such as how many worksheets we have, 
how many user accounts, how is the traffic during the last hour/day/week/month,
etc. We also need to set CPU time and memory limits, and enforce them.
</p>

<h3>Improve the Structure of Published Worksheets</h3>

<p>
The number of published worksheets is growing and thus we need to split them into 
thematic groups. The user should be able to choose for his/her worksheet tags 
from predefined keywords, or create own keywords. Search should be enabled 
by "author" and by "keyword".
</p>

<h3>Make Online Lab work natively on Android</h3>

<p>
Currently, the Online Lab will work there just in a web browser mode, 
but the user experience can be improved dramatically by creating a native
version for Android. 
</p>


<h3>Implement Javascript GUIs for engineering and scientific applications</h3>

<p>
We need to implement Javascript GUIs similar to <a href="http://hpfem.org/agros2d">Agros2D</a>
(see sample screenshot <a href="http://hpfem.org/agros2d/images/ubuntu-motor.png">here</a>) 
and make them run in the Online Lab. The Javascript GUIs do not need to 
be this general, we would rather prefer to have many simple GUIs that are designed 
to handle smaller model classes. They need to be easily customizable.
</p>


<h3>Implement Robot Karel in Javascript</h3>

<p>
This is a great tool to teach programming to kids. 
See its <a href="http://en.wikipedia.org/wiki/Karel_%28programming_language%29">Wikipedia page</a>.
